class SwitchCaseExample { // 5
     public static void main (String[] args) {
          int mode = 2;
          switch(mode){
               case 1:
                    System.out.println("แสดงผลแบบที่ 1");
                    break;
               case 2:
                    System.out.println("แสดงผลแบบที่ 2");
                    break;
               default:
                    System.out.println("แสดงผลแบบค่าเริ่มต้น");
        }
    }
}